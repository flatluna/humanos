function findingsFor<T extends { id: string; source: RoleAlignmentFinding['source'] }>(
  items: T[],
  category: RoleAlignmentFinding['category'],
  summaryFor: (item: T) => RoleAlignmentFinding['summary'],
): RoleAlignmentFinding[] {
  return items.map((item) => ({
    id: `finding-${item.id}`,
    category,
    proposedEntityId: item.id,
    summary: summaryFor(item),
    rationale: null,
    source: item.source,
    validationStatus: 'unvalidated',
    validationAuthority: null,
    stage: 'recommend',
  }));
}

